public class Dish {

    public String dishContent;
    public String dishDescription;
    public String dishId;
    public String dishIngredients;
    public String dishName;
    public String dishPrice;
    public String dishRatings;
    public String dishThumbnail;
    public String dishType;
    public String foodPreparationTime;

    public String getDishContent() {
        return dishContent;
    }

    public void setDishContent(String dishContent) {
        this.dishContent = dishContent;
    }

    public String getDishDescription() {
        return dishDescription;
    }

    public void setDishDescription(String dishDescription) {
        this.dishDescription = dishDescription;
    }

    public String getDishId() {
        return dishId;
    }

    public void setDishId(String dishId) {
        this.dishId = dishId;
    }

    public String getDishIngredients() {
        return dishIngredients;
    }

    public void setDishIngredients(String dishIngredients) {
        this.dishIngredients = dishIngredients;
    }

    public String getDishName() {
        return dishName;
    }

    public void setDishName(String dishName) {
        this.dishName = dishName;
    }

    public String getDishPrice() {
        return dishPrice;
    }

    public void setDishPrice(String dishPrice) {
        this.dishPrice = dishPrice;
    }

    public String getDishRatings() {
        return dishRatings;
    }

    public void setDishRatings(String dishRatings) {
        this.dishRatings = dishRatings;
    }

    public String getDishThumbnail() {
        return dishThumbnail;
    }

    public void setDishThumbnail(String dishThumbnail) {
        this.dishThumbnail = dishThumbnail;
    }

    public String getDishType() {
        return dishType;
    }

    public void setDishType(String dishType) {
        this.dishType = dishType;
    }

    public String getFoodPreparationTime() {
        return foodPreparationTime;
    }

    public void setFoodPreparationTime(String foodPreparationTime) {
        this.foodPreparationTime = foodPreparationTime;
    }
}