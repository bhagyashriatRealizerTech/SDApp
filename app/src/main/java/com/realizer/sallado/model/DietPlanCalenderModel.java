public class DietPlanCalenderModel {

    public String day;
    public String breakfast;
    public String lunch;
    public String dinner;
    public String imgBreakfast;
    public String imgLunch;
    public String imgDinner;
    public String imgSnacks;

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getBreakfast() {
        return breakfast;
    }

    public void setBreakfast(String breakfast) {
        this.breakfast = breakfast;
    }

    public String getLunch() {
        return lunch;
    }

    public void setLunch(String lunch) {
        this.lunch = lunch;
    }

    public String getDinner() {
        return dinner;
    }

    public void setDinner(String dinner) {
        this.dinner = dinner;
    }

    public String getImgBreakfast() {
        return imgBreakfast;
    }

    public void setImgBreakfast(String imgBreakfast) {
        this.imgBreakfast = imgBreakfast;
    }

    public String getImgLunch() {
        return imgLunch;
    }

    public void setImgLunch(String imgLunch) {
        this.imgLunch = imgLunch;
    }

    public String getImgDinner() {
        return imgDinner;
    }

    public void setImgDinner(String imgDinner) {
        this.imgDinner = imgDinner;
    }

    public String getImgSnacks() {
        return imgSnacks;
    }

    public void setImgSnacks(String imgSnacks) {
        this.imgSnacks = imgSnacks;
    }
}
