public class DietPlanModel {

    public String dietName;
    public String dietPrice;
    public String dietRatings;
    public String dietImp;
    public String dietPlan;
    public String dietIncludes;

    public String getDietName() {
        return dietName;
    }

    public void setDietName(String dietName) {
        this.dietName = dietName;
    }

    public String getDietPrice() {
        return dietPrice;
    }

    public void setDietPrice(String dietPrice) {
        this.dietPrice = dietPrice;
    }

    public String getDietRatings() {
        return dietRatings;
    }

    public void setDietRatings(String dietRatings) {
        this.dietRatings = dietRatings;
    }

    public String getDietImp() {
        return dietImp;
    }

    public void setDietImp(String dietImp) {
        this.dietImp = dietImp;
    }

    public String getDietPlan() {
        return dietPlan;
    }

    public void setDietPlan(String dietPlan) {
        this.dietPlan = dietPlan;
    }

    public String getDietIncludes() {
        return dietIncludes;
    }

    public void setDietIncludes(String dietIncludes) {
        this.dietIncludes = dietIncludes;
    }
}
